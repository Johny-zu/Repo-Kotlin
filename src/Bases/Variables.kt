package Bases

fun main() {
    var clientes = 10; //Se inicializa valor mutable

    clientes = 8 //odemos darle un valor de manera mas directa

    clientes = clientes + 3; //asi podemos sumar clientes
    clientes += 7 // sumamos clientes
    clientes -= 3 //restamos clientes
    clientes *= 2 //multiplicamos a los clientes
    clientes /= 3 //dividimos a los clientes

    println(clientes) //imprimiso clientes

    var con : Int //Asi podemos hacer una variable sin inicializar
    con = 3
    val pal : String = "Hola" //otra forma

    println(con)
    println(pal)
}